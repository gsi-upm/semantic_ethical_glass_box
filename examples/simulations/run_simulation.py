ARI_NAMESPACE = "https://gsi.upm.es/segb/robots/ari/v1/"
TIAGO_NAMESPACE = "https://gsi.upm.es/segb/robots/tiago/v1/"
DEFAULT_PUBLISH_URL = "http://localhost:5000"


@dataclass(slots=True)
class SimulationResult:
    """Artifacts generated by one base interaction."""

    graph: Graph
    human_uri: URIRef
    entry_shared_event_uri: URIRef
    shared_event_uri: URIRef
    ari_observation_uri: URIRef
    tiago_observation_uri: URIRef
    ari_response_uri: URIRef
    ari_namespace: str
    tiago_namespace: str


@dataclass(slots=True)
class PublishConfig:
    """Configuration used to publish logs to SEGB API."""

    base_url: str
    token: str | None = None
    user: str | None = None
    queue_file: str | None = None
    timeout_seconds: float = 20.0
    verify_tls: bool = True


def build_loggers(
    *,
    shared_event_resolver: SharedEventResolver | None = None,
) -> tuple[Graph, SemanticSEGBLogger, SemanticSEGBLogger]:
    """Builds an isolated in-memory graph and one logger per robot."""
    graph = Graph()
    policy = SharedEventPolicy(
        namespace="https://gsi.upm.es/segb/shared-events/",
        time_bucket_seconds=2,
    )
    ari_logger = SemanticSEGBLogger(
        base_namespace=ARI_NAMESPACE,
        robot_id="ari1",
        robot_name="ARI",
        graph=graph,
        namespace_prefix="ari",
        compact_resource_ids=True,
        shared_event_policy=policy,
        shared_event_resolver=shared_event_resolver,
    )
    tiago_logger = SemanticSEGBLogger(
        base_namespace=TIAGO_NAMESPACE,
        robot_id="tiago1",
        robot_name="TIAGo",
        graph=graph,
        namespace_prefix="tiago",
        compact_resource_ids=True,
        shared_event_policy=policy,
        shared_event_resolver=shared_event_resolver,
    )
    return graph, ari_logger, tiago_logger


def _run_entry_detection(
    *,
    ari_logger: SemanticSEGBLogger,
    tiago_logger: SemanticSEGBLogger,
    human_uri: URIRef,
    observed_at: datetime,
) -> tuple[AriDetectionResult, TiagoDetectionResult]:
    ari_entry_result = ari_detect_human_entry(
        logger=ari_logger,
        human_uri=human_uri,
        observed_at=observed_at,
    )
    tiago_entry_result = tiago_detect_human_entry(
        logger=tiago_logger,
        human_uri=human_uri,
        observed_at=observed_at,
    )
    if ari_entry_result.shared_event_uri != tiago_entry_result.shared_event_uri:
        raise RuntimeError("Shared context mismatch: ARI and TIAGo should resolve the same human-entry URI.")
    return ari_entry_result, tiago_entry_result


def _run_utterance_flow(
    *,
    ari_logger: SemanticSEGBLogger,
    tiago_logger: SemanticSEGBLogger,
    human_uri: URIRef,
    text: str,
    observed_at: datetime,
) -> tuple[AriMockResult, TiagoMockResult]:
    ari_result = ari_handle_human_utterance(
        logger=ari_logger,
        human_uri=human_uri,
        text=text,
        observed_at=observed_at,
    )
    tiago_result = tiago_handle_human_utterance(
        logger=tiago_logger,
        human_uri=human_uri,
        text=text,
        observed_at=observed_at,
    )
    if ari_result.shared_event_uri != tiago_result.shared_event_uri:
        raise RuntimeError("Shared context mismatch: ARI and TIAGo should resolve the same URI.")
    return ari_result, tiago_result


def run_basic_simulation(*, shared_event_resolver: SharedEventResolver | None = None) -> SimulationResult:
    """Runs one interaction scenario.

    Scenario:
    - Maria enters the room and both robots detect her.
    - Maria tells ARI she feels sad.
    - ARI detects sadness (speech + face), responds with support, then detects happiness.
    - TIAGo listens, evaluates engagement, and does not respond.
    """
    graph, ari_logger, tiago_logger = build_loggers(shared_event_resolver=shared_event_resolver)

    maria_uri = ari_logger.register_human("maria", first_name="Maria")
    entry_observed_at = datetime.now(timezone.utc)
    speech_observed_at = entry_observed_at + timedelta(seconds=4)

    ari_entry_result, _ = _run_entry_detection(
        ari_logger=ari_logger,
        tiago_logger=tiago_logger,
        human_uri=maria_uri,
        observed_at=entry_observed_at,
    )

    text = "ARI, I feel sad because I just received bad news."
    ari_result, tiago_result = _run_utterance_flow(
        ari_logger=ari_logger,
        tiago_logger=tiago_logger,
        human_uri=maria_uri,
        text=text,
        observed_at=speech_observed_at,
    )

    return SimulationResult(
        graph=graph,
        human_uri=maria_uri,
        entry_shared_event_uri=ari_entry_result.shared_event_uri,
        shared_event_uri=ari_result.shared_event_uri,
        ari_observation_uri=ari_result.observation_message_uri,
        tiago_observation_uri=tiago_result.observation_message_uri,
        ari_response_uri=ari_result.response_message_uri,
        ari_namespace=ARI_NAMESPACE,
        tiago_namespace=TIAGO_NAMESPACE,
    )


