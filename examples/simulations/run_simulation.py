def publish_graph(graph: Graph, *, config: PublishConfig) -> dict[str, Any]:
    """Clears previous KG content and publishes an RDF graph."""
    publisher = SEGBPublisher(
        base_url=config.base_url,
        token=config.token,
        default_user=config.user,
        timeout_seconds=config.timeout_seconds,
        verify_tls=config.verify_tls,
        queue_file=config.queue_file,
    )
    cleanup_response = publisher.delete_all_ttls(user=config.user)
    publish_response = publisher.publish_graph(graph, user=config.user)
    return {
        "published": True,
        "cleanup_response": cleanup_response,
        "publish_endpoint": f"{config.base_url.rstrip('/')}/ttl",
        "publish_response": publish_response,
        "graph_triplet_count": len(graph),
    }


def publish_simulation_result(simulation_result: SimulationResult, *, config: PublishConfig) -> dict[str, Any]:
    """Publishes the simulation graph into SEGB backend."""
    return publish_graph(simulation_result.graph, config=config)


def build_publish_config_from_args(args: argparse.Namespace) -> PublishConfig | None:
    """Builds `PublishConfig` from CLI arguments and environment variables."""
    base_url = args.publish_url or os.getenv("SEGB_API_URL") or DEFAULT_PUBLISH_URL
    if not base_url:
        return None

    token = args.token if args.token is not None else os.getenv("SEGB_API_TOKEN")
    user = args.user if args.user is not None else os.getenv("SEGB_LOG_USER")
    queue_file = args.queue_file if args.queue_file is not None else os.getenv("SEGB_QUEUE_FILE")
    timeout_env = os.getenv("SEGB_PUBLISH_TIMEOUT")
    timeout_seconds = args.timeout_seconds if args.timeout_seconds is not None else float(timeout_env or 20.0)
    verify_tls = not args.insecure

    return PublishConfig(
        base_url=base_url,
        token=token,
        user=user,
        queue_file=queue_file,
        timeout_seconds=timeout_seconds,
        verify_tls=verify_tls,
    )


def build_shared_event_resolver(
    publish_config: PublishConfig | None,
) -> SharedEventResolver | None:
    if publish_config is None:
        return None

    return HTTPSharedContextResolver(
        base_url=publish_config.base_url,
        token=publish_config.token,
        timeout_seconds=min(5.0, publish_config.timeout_seconds),
        verify_tls=publish_config.verify_tls,
        raise_on_error=True,
    )


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description=(
            "Run use case 01 (basic interaction), optionally publish to SEGB, and optionally export Turtle."
        )
    )
    add_ttl_output_arguments(parser)
    add_publish_arguments(parser, include_no_publish=True)
    return parser.parse_args()


def main() -> None:
    args = parse_args()
    publish_config = None if args.no_publish else build_publish_config_from_args(args)
    shared_event_resolver = build_shared_event_resolver(publish_config)

    basic_result = run_basic_simulation(shared_event_resolver=shared_event_resolver)

    if publish_config is not None:
        report = publish_simulation_result(basic_result, config=publish_config)
        print(json.dumps(report, ensure_ascii=True, indent=2))

    ttl_text = graph_to_ttl_text(basic_result.graph)

    output_path = write_ttl_output(ttl_text, args.ttl_output)
    if output_path is not None:
        print(f"Wrote Turtle to: {output_path}")

    print("Use case: 01-basic-interaction")
    print(f"SharedEvent URI: {basic_result.shared_event_uri}")
    print(f"Triples generated: {len(basic_result.graph)}")

    if not args.no_print_ttl:
        print(ttl_text)


if __name__ == "__main__":
    main()
